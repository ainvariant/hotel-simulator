export const FLOORS = [2, 3, 4, 5];
export const ROOMS_PER_FLOOR = 10;

export const generateRoomList = () => {
  const rooms: string[] = [];
  for (const floor of FLOORS) {
    for (let i = 1; i <= ROOMS_PER_FLOOR; i++) {
      const roomNum = `${floor}${i.toString().padStart(2, '0')}`;
      rooms.push(roomNum);
    }
  }
  return rooms;
};

